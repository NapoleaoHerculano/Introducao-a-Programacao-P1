#exiba uma mensagem informando se ele é positivo, negativo ou neutro.

numero=int(input("Digite um número:"))

if numero > 0:
    print("Número Positivo")
elif numero < 0:
    print("Número Negativo")
else:
    print("Número Neutro")
    
    
